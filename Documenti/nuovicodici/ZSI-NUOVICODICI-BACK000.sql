CREATE PROCEDURE ITA_UPDATECODICISOST_BACK  (@OLDCODART VARCHAR(50), @NEWCODART VARCHAR(50) ) AS
	DECLARE @CSQL_S AS NVARCHAR(4000)
	DECLARE @CSQL_U AS NVARCHAR(4000)
	DECLARE @TABELLA AS VARCHAR(500)
	
	PRINT @OLDCODART + '  ' + @NEWCODART

	BEGIN TRANSACTION

	IF LEFT(@oldcodart, 1) IN ('3', '4')
	BEGIN
	
		--PRINT 'SERIE 30000, 40000'
		
		-- TRASFORMO TIPOLOGIE A VARIANTI IN PUNTUALE
		UPDATE ANAGRAFICAARTICOLI SET ARTTIPOLOGIA = 0 WHERE CODICE = @NEWCODART AND ARTTIPOLOGIA = 1
	
		DECLARE rSqlA CURSOR LOCAL KEYSET FOR 
	
		SELECT TABELLA
		, 'SELECT ' + '*' + ' INTO BACK.DBO.' + TABELLA + '_' + @OLDCODART + '_' + CAMPO + ' FROM ' + TABELLA + ' WHERE '+ CAMPO + ' = ''' + @OLDCODART + ''''  AS STRSQL_S
		, 'UPDATE ' + TABELLA + ' SET ' + CAMPO + ' = ''' + @NEWCODART + ''' WHERE '+ CAMPO + ' = ''' + @OLDCODART + '''' AS STRSQL
		--, * 
		FROM ITA_CODICISOST i 
		WHERE i.SEL = 1
		
	
	--	ORDER BY C.COLUMN_NAME
		OPEN rSqlA
	
		FETCH NEXT from rSqlA INTO @TABELLA,  @CSQL_S, @CSQL_U
		WHILE (@@FETCH_STATUS <> -1)
			BEGIN
			
				--PRINT @CSQL_S
				--EXECUTE sp_executesql  @CSQL_S 
				
				--PRINT @TABELLA
				
				IF @TABELLA <> 'DESCRARTICOLI'
				BEGIN
					IF RIGHT(@OLDCODART, 3) = 'XXX' AND @TABELLA IN ('ITA_TABREGISTRONOTIFICHEBSC', 'TABLOTTIRIORDINO') 
					BEGIN
						EXEC (@CSQL_U)
					END 
				END
				--PRINT @@ROWCOUNT
		
				FETCH NEXT from rSqlA INTO @TABELLA, @CSQL_S, @CSQL_U
			END
	
		CLOSE rSqlA
		DEALLOCATE rSqlA
			
	END



	IF LEFT(@oldcodart, 1) = '2'
	BEGIN
	
		DELETE FROM dbo.TIPOLOGIEARTICOLI
		WHERE CODICEART = LEFT(@NEWCODART, 5) AND NUMEROTIP = 2
		
		UPDATE dbo.TIPOLOGIEARTICOLI
		SET NUMEROTIP = 2
		WHERE CODICEART = LEFT(@NEWCODART, 5) AND NUMEROTIP = 3
		
		DECLARE rSqlA CURSOR LOCAL KEYSET FOR 
	
		SELECT TABELLA
		, 'SELECT ' + '*' + ' INTO BACK.DBO.' + TABELLA + '_' + @OLDCODART + '_' + CAMPO + ' FROM ' + TABELLA + ' WHERE '+ CAMPO + ' = ''' + @OLDCODART + ''''  AS STRSQL_S
		, (CASE 
			WHEN TABELLA = 'GESTIONEPREZZI' THEN
			--	'UPDATE ' + TABELLA + ' SET CODART = ''' + @OLDCODART + '#??????'', CODARTRIC = ''' + @OLDCODART + '#___%'' WHERE INIZIOVALIDITA = FINEVALIDITA AND '+ CAMPO + ' = ''' + @OLDCODART + ''''			WHEN TABELLA = 'RELAZIONICFV' AND CAMPO = 'VARIANTI' THEN
				'UPDATE ' + TABELLA + ' SET VARIANTI = ''' + SUBSTRING(@NEWCODART, CHARINDEX('#', @NEWCODART) + 1, LEN(@NEWCODART) - CHARINDEX('#', @NEWCODART)) + ''' WHERE ARTICOLO  + ''#'' + ' + CAMPO + ' = ''' + @OLDCODART + ''''
			ELSE
				'UPDATE ' + TABELLA + ' SET ' + CAMPO + ' = ''' + @NEWCODART + ''' WHERE '+ CAMPO + ' = ''' + @OLDCODART + ''''
			END)  
			AS STRSQL
		--, * 
		--, 'UPDATE ITA_CODICISOST SET SEL = 0 WHERE TABELLA = ''' + TABELLA + ''' AND (SELECT COUNT(*) FROM ' + TABELLA + ' WHERE ' + CAMPO + ' IN (SELECT Z.CODART FROM ZS_GENERAARTICOLI z)) = 0' AS STRSQL
		FROM ITA_CODICISOST i 
		WHERE I.SEL = 1
	
	--	ORDER BY C.COLUMN_NAME
		OPEN rSqlA
	
		FETCH NEXT from rSqlA INTO @TABELLA,  @CSQL_S, @CSQL_U
		WHILE (@@FETCH_STATUS <> -1)
			BEGIN
			
				--PRINT @CSQL_S
				
				--EXECUTE sp_executesql  @CSQL_S 
				
				--PRINT @TABELLA
				--PRINT @CSQL_U
				
				IF @TABELLA <> 'DESCRARTICOLI'
				BEGIN
					IF RIGHT(@OLDCODART, 3) = 'XXX' AND @TABELLA IN ('ITA_TABREGISTRONOTIFICHEBSC', 'TABLOTTIRIORDINO') 
					BEGIN
						EXEC (@CSQL_U)
					END 
				END	
				--PRINT @@ROWCOUNT
		
				FETCH NEXT from rSqlA INTO @TABELLA, @CSQL_S, @CSQL_U
			END
	
		CLOSE rSqlA
		DEALLOCATE rSqlA
		
	END
	
	IF @@error > 0 
    BEGIN
        ROLLBACK TRANSACTION
        RETURN @@error -- Errore in inserimento radice dell'albero
    END

    COMMIT TRANSACTION	
GO

