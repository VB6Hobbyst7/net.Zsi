CREATE PROCEDURE [dbo].[ITA_SP_UPDATE_DISPONIBILITA]
(

@idtesta DECIMAL(10)
, @idriga DECIMAL(5)
, @disponibilita AS DECIMAL(16,6)
, @posizionamento VARCHAR(50) = ''
)
AS

BEGIN

	SET NOCOUNT ON

	DECLARE @codart VARCHAR(50)
	DECLARE @codimballo VARCHAR(10)
	DECLARE @qtagestres DECIMAL (16,6)
	
	SELECT TOP 1 @codart = CODART
		, @codimballo = CODIMBALLO
		, @qtagestres = QTAGESTRES 
	FROM RIGHEDOCUMENTI WITH (NOLOCK) WHERE IDTESTA = @idtesta AND IDRIGA = @idriga

	DECLARE @codclifor VARCHAR(7)
	SELECT @codclifor = CODCLIFOR
	FROM TESTEDOCUMENTI WHERE PROGRESSIVO = @idtesta 
	
		
	UPDATE EXTRARIGHEDOC
	SET MAGSTATOM05 = (CASE WHEN MAGSTATOM05 = 0 THEN 1 ELSE MAGSTATOM05 END), POSIZIONAMENTO = @posizionamento, DISPONIBILITA = @disponibilita
	WHERE IDTESTA = @idtesta AND IDRIGA = @idriga
	
	DELETE  dbo.ZSI_DISPONIBILITAM05
	WHERE IDTESTA = @idtesta AND IDRIGA = @idriga

	INSERT INTO dbo.ZSI_DISPONIBILITAM05 (CODCLIFOR, CODART, QTAGESTRES, DISPONIBILITA, CODIMBALLO, UTENTEMODIFICA, DATAMODIFICA, IDTESTA, IDRIGA)
	VALUES (@codclifor, @codart, @qtagestres, @disponibilita, @codimballo, 'trm', getdate(), @idtesta, @idriga)
--	WHERE NOT EXISTS(SELECT 1 FROM ZSI_DISPONIBILITAM05 x WHERE x.CODCLIFOR = @codclifor AND x.CODART = @codart AND x.CODIMBALLO = @codimballo)
	
	/*
	UPDATE ZSI_DISPONIBILITAM05
	SET DISPONIBILITA = @disponibilita
	WHERE CODCLIFOR = @codclifor AND CODART = @codart AND CODIMBALLO = @codimballo
	
	UPDATE ZSI_DISPONIBILITAM05
	SET QTAGESTRES = ISNULL((SELECT sum(x.qtagestres) FROM EXCEL_PORTAFOGLIOORDINI x WHERE x.CODCLIFOR = @codclifor AND x.ARTICOLO = @codart AND CODIMBALLO = @codimballo), 0)
	WHERE CODCLIFOR = @codclifor AND CODART = @codart

	*/
	
					
	RETURN

END
GO

